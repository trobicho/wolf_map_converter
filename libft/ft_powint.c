int	ft_powint(int x, int p)
{
	int r;

	r = x;
	if (p == 0)
		return (1);
	if (p < 0)
		return (0);
	else
	{
		while (p > 1)
		{
			r *= x;
			p--;
		}
	}
	return (r);
}
